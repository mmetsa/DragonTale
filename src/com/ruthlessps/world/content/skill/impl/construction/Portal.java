package com.ruthlessps.world.content.skill.impl.construction;

/**
 * 
 * @author Owner Blade
 */
public class Portal {

	private int id, roomX, roomY, roomZ;
	private int type;

	public int getId() {
		return id;
	}

	public int getRoomX() {
		return roomX;
	}

	public int getRoomY() {
		return roomY;
	}

	public int getRoomZ() {
		return roomZ;
	}

	public int getType() {
		return type;
	}

	public void setId(int id) {
		this.id = id;
	}

	public void setRoomX(int roomX) {
		this.roomX = roomX;
	}

	public void setRoomY(int roomY) {
		this.roomY = roomY;
	}

	public void setRoomZ(int roomZ) {
		this.roomZ = roomZ;
	}

	public void setType(int type) {
		this.type = type;
	}

}
